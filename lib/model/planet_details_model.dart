  final String name;
  final String image;
  final String model3d;
  final String title;
  final String about;
  final String distanceFromSunKm;
  final String lengthOfDayHours;
  final String orbitalPeriodYears;
  final String radiusKm;
  final String massKg;
  final String gravity;
  final String surfaceAreaKm2;

